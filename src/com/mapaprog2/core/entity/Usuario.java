public class Usuario {
    private String campoCadNome;
    private String campoCadLogin;
    private String campoCadEmail;
    private String campoCadSenha;

    public String getCampoCadNome() {
        return campoCadNome;
    }

    public void setCampoCadNome(String campoCadNome) {
        this.campoCadNome = campoCadNome;
    }

    public String getCampoCadLogin() {
        return campoCadLogin;
    }

    public void setCampoCadLogin(String campoCadLogin) {
        this.campoCadLogin = campoCadLogin;
    }

    public String getCampoCadEmail() {
        return campoCadEmail;
    }

    public void setCampoCadEmail(String campoCadEmail) {
        this.campoCadEmail = campoCadEmail;
    }

    public String getCampoCadSenha() {
        return campoCadSenha;
    }

    public void setCampoCadSenha(String campoCadSenha) {
        this.campoCadSenha = campoCadSenha;
    }
    
    
}
